    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        // <editor-fold defaultstate="collapsed" desc="Add the Account of this Employee">
        if(first.getText().equals("") || last.getText().equals("") || un.getText().equals("") || pas.getText().equals("") || al.getSelectedItem().toString().equals("Select")){
            JOptionPane.showMessageDialog(null, "Please Fill up all the Fields");
        }else{
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to add this account ?","Add Account",JOptionPane.YES_NO_OPTION);
        if(p == 0){
            try{
                conn = db.db();
                pst = conn.prepareStatement("INSERT INTO payroll_schema.account_tbl "
                    + "VALUES (?,?,?,?,?,?);");
                pst.setString(1, id.getText());
                pst.setString(2, first.getText());
                pst.setString(3, last.getText());
                pst.setString(4, un.getText());
                pst.setString(5, pas.getText());
                pst.setString(6, al.getSelectedItem().toString());
                pst.execute();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            JOptionPane.showMessageDialog(null, "Added Successfully");
        }
        }
        //</editor-fold>
    }//GEN-LAST:event_addActionPerformed

