public void detail(){
    try{
        conn = db.db();
        pst = conn.prepareStatement("SELECT * FROM payroll_schema.subsss_tbl WHERE sss_id = ? and sss_brakcet = ?;");
        pst.setString(1,bracket.getText() );
        pst.setString(2,bracket.getText() );
        rs = pst.executeQuery();

        while(rs.next()){
            min.setText(rs.getString("sss_min"));
            max.setText(rs.getString("sss_max"));
            credit.setText(rs.getString("sss_salarycredit"));
            er.setText(rs.getString("er"));
            ee.setText(rs.getString("ee"));
            ec.setText(rs.getString("ec"));
            total.setText(rs.getString("total"));
        }
    }
    catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
}
