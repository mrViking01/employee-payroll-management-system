public void detail(){
    try{
        conn = db.db();
        pst = conn.prepareStatement("SELECT * FROM payroll_schema.subtax_tbl WHERE tax_id = ? and tax_bracket = ?;");
        pst.setString(1,bracket.getText());
        pst.setString(2,bracket.getText());
        rs = pst.executeQuery();

        while(rs.next()){
            min.setText(rs.getString("tax_min"));
            max.setText(rs.getString("tax_max"));
            base.setText(rs.getString("tax_base"));
            excess.setText(rs.getString("tax_excess"));
            rate.setText(rs.getString("tax_rate"));
        }
    }
    catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
}
