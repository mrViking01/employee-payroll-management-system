    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(min.getText().equals("") || max.getText().equals("") ||  base.getText().equals("") || excess.getText().equals("") || rate.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please Fill up all the Fields");
        }else{
            int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to add this new bracket ?","Add Bracket",JOptionPane.YES_NO_OPTION);
            if(p == 0){
                try{
                    conn = db.db();
                    pst = conn.prepareStatement("INSERT INTO payroll_schema.subtax_tbl "
                        + "VALUES (?,?,?,?,?,?,?);");
                    pst.setString(1, bracket.getText());
                    pst.setString(2, bracket.getText());
                    pst.setString(3, min.getText());
                    pst.setString(4, max.getText());
                    pst.setString(5, base.getText());
                    pst.setString(7, rate.getText());
                    pst.setString(6, excess.getText());
                    pst.execute();
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
                JOptionPane.showMessageDialog(null, "Added Successfully");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

