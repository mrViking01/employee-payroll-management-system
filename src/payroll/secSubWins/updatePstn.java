public void detail(){
    try{
        String a = i;
        conn = db.db();
        pst = conn.prepareStatement("SELECT * FROM payroll_schema.position_tbl WHERE position = ?");
        pst.setString(1, a);
        rs = pst.executeQuery();
        while(rs.next()){
            bs2.setText(rs.getString("basic_salary"));
            sr1.setText(rs.getString("salary_rate"));
            noe.setText(rs.getString("no_of_emp"));
        }
    }
    catch(Exception e){JOptionPane.showMessageDialog(null,e);} 
}
