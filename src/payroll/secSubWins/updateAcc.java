    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        // <editor-fold defaultstate="collapsed" desc="Update Account of this Employee">
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to update the account of this employee?","Update Account",JOptionPane.YES_NO_OPTION);
        if(p == 0){
            try{
                conn = db.db();
                pst = conn.prepareStatement("UPDATE payroll_schema.account_tbl SET firstname = ?, "
                    + "lastname = ?, username = ?, password = ?,  division = ? WHERE emp_acc_id = ?");
                pst.setString(6, id1.getText());
                pst.setString(1, first.getText());
                pst.setString(2, last.getText());
                pst.setString(3, un.getText());
                pst.setString(4, pas.getText());
                pst.setString(5, al.getSelectedItem().toString());
                pst.execute();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            JOptionPane.showMessageDialog(null, "Updated Successfully");
        }
        //</editor-fold>
    }//GEN-LAST:event_updateActionPerformed

