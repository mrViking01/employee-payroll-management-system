    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        // <editor-fold defaultstate="collapsed" desc="Update Details of Employee's Position">
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to update the detail of this position?","Update Record",JOptionPane.YES_NO_OPTION);
        if(p == 0){
            String eid =   empid1.getText();
            String last =    empln.getText();
            String first =    empfn.getText();
            String middle = empmn.getText();
            String position = po.getSelectedItem().toString();
            String slry = sar.getText();
            String bsc = bs.getText();
            try{
                conn = db.db();
                pst = conn.prepareStatement("UPDATE payroll_schema.emp_tbl SET "
                    + "lastname = '"+last+"', "
                    + "firstname = '"+first+"', "
                    + "middlename = '"+middle+"', "
                    + "salary_rate = "+slry+", "
                    + "basic_salary = "+bsc+", "
                    + "position = '"+position+"' WHERE emp_id = "+eid+";");
                pst.execute();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            
            JOptionPane.showMessageDialog(null,"Details is Updated successfully");
        }
        //</editor-fold>
    }//GEN-LAST:event_updateActionPerformed

