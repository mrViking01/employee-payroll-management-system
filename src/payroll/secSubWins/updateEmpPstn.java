public void detail(){
    try{
        String f =ids;
            conn = db.db();
            pst = conn.prepareStatement("SELECT * FROM payroll_schema.emp_tbl where emp_id = "+f+";");
            rs = pst.executeQuery();
            while(rs.next()){
                po.setSelectedItem(rs.getString("position"));
                empfn.setText(rs.getString("firstname"));
                empln.setText(rs.getString("lastname"));
                empmn.setText(rs.getString("middlename"));
                empid1.setText(rs.getString("emp_id"));
                sar.setText(rs.getString("salary_rate"));
                bs.setText(rs.getString("basic_salary"));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
}
