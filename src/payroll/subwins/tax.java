    private void newemp1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newemp1ActionPerformed
        // TODO add your handling code here:
        int p = JOptionPane.showConfirmDialog(null, "Do you want to add to Tax table ?","Add Record",JOptionPane.YES_NO_OPTION);
        if(p == 0){
            try{
                conn = db.db();
                pst = conn.prepareStatement("SELECT * FROM payroll_schema.subtax_tbl;");
                rs = pst.executeQuery();
                
                while(rs.next()){
                 int recentid  = rs.getInt("tax_id");
                 int newid = recentid + 1;
                 brace.setText(""+newid);
                }
                }
            catch(Exception e){JOptionPane.showMessageDialog(null, e);}
            state = 1;
            brace.setEditable(true);
            min.setEditable(true);
            max.setEditable(true);
            base.setEditable(true);
            excess.setEditable(true);
            rate.setEditable(true);

            save1.setEnabled(true);
            cancel1.setEnabled(true);
            newemp1.setEnabled(false);
            edit.setEnabled(false);
            jButton4.setEnabled(false);
            table.setEnabled(false);

            brace.setText("");
            min.setText("");
            max.setText("");
            base.setText("");
            excess.setText("");
            rate.setText("");
        }
    }//GEN-LAST:event_newemp1ActionPerformed

